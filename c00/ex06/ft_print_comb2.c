#include <unistd.h>

void	ft_print_comb2(void)
{
	char	v;
	int		a;
	int		b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			v = a / 10 + '0';
			write(1, &v, 1);
			v = a % 10 + '0';
			write(1, &v, 1);
			write(1, " ", 1);
			v = b / 10 + '0';
			write(1, &v, 1);
			v = b % 10 + '0';
			write(1, &v, 1);
			b++;
			if (a != 98)
				write(1, ", ", 2);
		}
		a++;
	}
}
